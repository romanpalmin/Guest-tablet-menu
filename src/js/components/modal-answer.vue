        methods: {
            printPhone(digit) {
                let len = this.currentPhone.length;
                if (len < 10) {
                    this.currentPhone = '' + this.currentPhone + digit;
                }
            },
            deletePhone() {
                let len = this.currentPhone.length;
                console.log(this.currentPhone);
